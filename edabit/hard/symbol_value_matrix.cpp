#include <iostream>
#include <string>
#include <vector>
using namespace std;

int checkScore(vector<vector<string>> str) {
	int result = 0;
    for (int i = 0; i < str.size(); ++i) {
        for (int j = 0; j < str[0].size(); ++j) {
            if (str[i][j] == "#")
                result = result + 5;
            else if (str[i][j] == "O")
                result = result + 3;
            else if (str[i][j] == "X")
                result = result + 1;
            else if (str[i][j] == "!")
                result = result - 1;
            else if (str[i][j] == "!!")
                result = result - 3;
            else if (str[i][j] == "!!!")
                result = result - 5;
        }
    }

    return result < 0 ? 0 : result;
}

